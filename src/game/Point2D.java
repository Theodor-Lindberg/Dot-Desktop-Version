package game;

public class Point2D
{
    private float x, y;

    public Point2D(final float x, final float y) {
	this.x = x;
	this.y = y;
    }

    public float getX() {
	return x;
    }

    public void setX(final float x) {
	this.x = x;
    }

    public float getY() {
	return y;
    }

    public void setY(final float y) {
	this.y = y;
    }
}
